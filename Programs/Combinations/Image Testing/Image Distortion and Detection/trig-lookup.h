float * lookup_y_x( int y, int x )
{
    if( x == 0 )
    {
        if( y > 0 )
        {
            return lookup( PI_2_LKP );
        }
        else if( y < 0 )
        {
            return lookup( PI_2_LKP );
        }
        else
        {
            return sin_atan_lookup_h[ 0 ];
        }
    }
    
    float y_x = ab( y / x );
    
    if( x > 0 )
    {
        return sin_atan_lookup_h[ lkp( y_x ) ];
    }
    else if( x < 0 )
    {
        if( y >= 0 )
        {
            return sin_atan_lookup_h[ lkp( y_x + PI ) ];
        }
        else
        {
            return sin_atan_lookup_h[ lkp( y_x - PI ) ];
        }
    }
}

